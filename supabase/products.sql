-- ============================================================================
--  Saponé — products table (run this once in Supabase → SQL Editor)
--  Moves the catalogue into the database so it can be managed from /admin.
--  Public can read; only the admin API (service-role key) can write.
-- ============================================================================

create table if not exists public.products (
  id           bigint generated by default as identity primary key,
  slug         text unique not null,
  name         text not null,
  tagline      text,
  scent        text,
  price        numeric not null default 0,
  category     text not null default 'Nourish',
  accent       text not null default '#c1714e',
  accent2      text not null default '#7d3f28',
  rating       numeric not null default 5,
  reviews      int not null default 0,
  badge        text,
  weight       text default '120g',
  ingredients  text[] not null default '{}',
  benefits     text[] not null default '{}',
  description  text,
  featured     boolean not null default false,
  sort_order   int not null default 0,
  created_at   timestamptz not null default now()
);

create index if not exists products_sort_order_idx on public.products (sort_order);

alter table public.products enable row level security;

-- Anyone can read the catalogue.
drop policy if exists "Products are viewable by everyone" on public.products;
create policy "Products are viewable by everyone"
  on public.products for select using (true);

-- No insert/update/delete policies on purpose: regular users can't write.
-- The admin API uses the service-role key, which bypasses RLS.
